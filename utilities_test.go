func Test_appendError(t *testing.T) {
	var a []FileError
	b := &FileError{"Hello", []interface{}{1, 2}}
	c := &FileError{"Goodbye", []interface{}{3, 4}}
	a = appendError(a, b, c)
	if len(a) != 2 {
		t.Errorf("appendError(): Should return a slice with length of 2!")
	}
}

func Test_inSlice(t *testing.T) {
	var list = []struct {
		field       string
		expectation bool
	}{
		{"abc", true},
		{"123", true},
		{"Abc", false},
		{"@bc", false},
		{"Ã£bc", false},
		{"314", false},
		{"-3.14", false},
	}

	testList := []string{"abc", "def", "ghi", "jkl", "123", "3.14"}
	for _, l := range list {
		valid := inSlice(testList, l.field)
		if l.expectation != valid {
			t.Errorf("inSlice(list, %s): Valid[%t]. Expected: %t", l.field, valid, l.expectation)
		}
	}
}

func Test_getFileExtension(t *testing.T) {
	var list = []struct {
		name string
		ext  string
	}{
		{"a.jpeg", "jpeg"},
		{"a.JPEG", "jpeg"},
		{"a.tar.gz", "gz"},
		{"something.thing.blah.that", "that"},
		{"something.one-two-three", "one-two-three"},
	}
	for _, l := range list {
		if ext := getFileExtension(l.name); ext != l.ext {
			t.Errorf("getFileExtension(%s): Returned[%s]. Expected: %t", l.name, ext, l.ext)
		}
	}
}

// utilities for testing embedded files (see below the functions for base64 encoded strings of 3 files)
type testFile struct {
	fileBase64Str string
	fieldname     string
	filename      string
}

func setupHTTPRequestBody(tf ...*testFile) (*bytes.Buffer, string) {
	body := &bytes.Buffer{}
	multiWriter := multipart.NewWriter(body)
	var fileWriter io.Writer
	var err error
	var ioReader io.Reader

	for _, i := range tf {
		ioReader = base64.NewDecoder(base64.StdEncoding, strings.NewReader(i.fileBase64Str))

		// file #1, simulate form field <input type="file" name="THIS_THING">
		if fileWriter, err = multiWriter.CreateFormFile(i.fieldname, i.filename); err != nil {
			log.Fatalf("Failed to create io.Writer: %s", err)
		}

		if _, err = io.Copy(fileWriter, ioReader); err != nil {
			log.Fatalf("Failed to copy io.Reader to io.Writer: %s", err)
		}
	}

	if err = multiWriter.Close(); err != nil {
		log.Fatalf("Failed to close multipart.Writer: %s", err)
	}

	return body, multiWriter.FormDataContentType()
}

func setupRequestMultipartForm(tf ...*testFile) *http.Request {
	body, contentType := setupHTTPRequestBody(tf...)
	var req *http.Request
	var err error

	if req, err = http.NewRequest("POST", "", body); err != nil {
		log.Fatalf("http.NewRequest: %s", err)
	}
	req.Header.Set("Content-Type", contentType)

	if err = req.ParseMultipartForm(1024 * 1024); err != nil {
		log.Fatalf("Request.ParseMultipartForm(): %s", err)
	}

	return req
}

